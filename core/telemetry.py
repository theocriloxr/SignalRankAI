def prometheus_metrics_text() -> str:
    return generate_latest().decode("utf-8", errors="replace")


def prometheus_content_type() -> str:
    return CONTENT_TYPE_LATEST


def observe_engine_cycle(seconds: float) -> None:
    try:
        ENGINE_CYCLE_SECONDS.observe(max(0.0, float(seconds)))
    except Exception:
        logger.debug("[telemetry] failed to observe engine cycle", exc_info=True)


def observe_engine_task(asset: str, timeframe: str, seconds: float, outcome: str = "ok") -> None:
    try:
        ENGINE_TASK_SECONDS.labels(asset=str(asset or "unknown"), timeframe=str(timeframe or "unknown"), outcome=str(outcome or "ok")).observe(max(0.0, float(seconds)))
    except Exception:
        logger.debug("[telemetry] failed to observe engine task", exc_info=True)


def observe_signal_dispatch(seconds: float, tier: str, regime: str | None = None, status: str = "ok") -> None:
    tier_label = str(tier or "unknown").lower()
    regime_label = str(regime or "unknown").lower()
    status_label = str(status or "ok").lower()
    try:
        SIGNAL_DISPATCH_SECONDS.labels(tier=tier_label, regime=regime_label, status=status_label).observe(max(0.0, float(seconds)))
        SIGNAL_DISPATCH_TOTAL.labels(tier=tier_label, regime=regime_label, status=status_label).inc()
    except Exception:
        logger.debug("[telemetry] failed to observe dispatch", exc_info=True)


def observe_signal_generated(asset: str, timeframe: str) -> None:
    try:
        SIGNAL_GENERATED_TOTAL.labels(asset=str(asset or "unknown").upper(), timeframe=str(timeframe or "unknown")).inc()
    except Exception:
        logger.debug("[telemetry] failed to observe generated signal", exc_info=True)


def observe_ml_confidence(value: float | None) -> None:
    if value is None:
        return
    try:
        ML_CONFIDENCE.observe(max(0.0, min(1.0, float(value))))
    except Exception:
        logger.debug("[telemetry] failed to observe ml confidence", exc_info=True)


def observe_http_request(method: str, route: str, status: int | str, seconds: float) -> None:
    try:
        HTTP_REQUEST_SECONDS.labels(method=str(method or "GET").upper(), route=str(route or "unknown"), status=str(status or "200")).observe(max(0.0, float(seconds)))
    except Exception:
        logger.debug("[telemetry] failed to observe http request", exc_info=True)


def set_exchange_api_health(provider: str, healthy: bool = True) -> None:
    try:
        EXCHANGE_API_HEALTH.labels(provider=str(provider or "unknown").lower()).set(1 if healthy else 0)
    except Exception:
        logger.debug("[telemetry] failed to set exchange health", exc_info=True)


def set_exchange_rate_limit(provider: str, remaining: float | None = None, limit: float | None = None) -> None:
    provider_label = str(provider or "unknown").lower()
    try:
        if remaining is not None:
            EXCHANGE_RATE_LIMIT_REMAINING.labels(provider=provider_label).set(max(0.0, float(remaining)))
        if limit is not None:
            EXCHANGE_RATE_LIMIT_LIMIT.labels(provider=provider_label).set(max(0.0, float(limit)))
    except Exception:
        logger.debug("[telemetry] failed to set rate limit", exc_info=True)


def init_tracer(service_name: str = "signalrankai"):
    """Initialize OpenTelemetry tracing if the optional packages are installed.

    The function is intentionally fail-open: if OTel is unavailable, it returns
    `None` and the rest of the service keeps running.
    """
    if trace is None or TracerProvider is None:
        return None
    if str(os.getenv("OTEL_SDK_DISABLED", "0")).strip().lower() in {"1", "true", "yes", "on"}:
        return None

    try:
        resource = Resource.create({"service.name": service_name}) if Resource is not None else None
        provider = TracerProvider(resource=resource) if resource is not None else TracerProvider()
        endpoint = (os.getenv("OTEL_EXPORTER_OTLP_ENDPOINT") or os.getenv("OTEL_EXPORTER_OTLP_TRACES_ENDPOINT") or "").strip()
        if endpoint and OTLPSpanExporter is not None:
            exporter = OTLPSpanExporter(endpoint=endpoint)
            provider.add_span_processor(BatchSpanProcessor(exporter))
        elif ConsoleSpanExporter is not None:
            provider.add_span_processor(BatchSpanProcessor(ConsoleSpanExporter()))
        trace.set_tracer_provider(provider)
        return trace.get_tracer(service_name)
    except Exception:
        logger.debug("[telemetry] otel init failed", exc_info=True)
        return None


@contextmanager
def trace_span(name: str, **attributes: Any) -> Iterator[Any]:
    tracer = init_tracer()
    if trace is None or tracer is None:
        yield None
        return
    with tracer.start_as_current_span(name) as span:
        try:
            for key, value in attributes.items():
                try:
                    span.set_attribute(str(key), value)
                except Exception:
                    continue
        except Exception:
            pass
        yield span
